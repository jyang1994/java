/**
 * 计算机类，用于播放音乐和聊天
 * @author 张三
 * @version 1.0	
 */
public class PC {

	/**
	 * 播放音乐
	 * @param musicName 用于播放的音乐名称
	 */
	public void playMusic(String musicName) {
		
		System.out.println("正在播放《" + musicName + "》音乐");
		
	}
	
	/**
	 * 上网聊天
	 * @param name 聊天人姓名
	 */
	public void chat(String name) {
		
		System.out.println("正在和" + name + "聊天");
		
	}
	
}
